package atos.knowledgelab.capture.persistence;


import java.io.FileNotFoundException;
import java.io.FileReader;
import java.io.FileWriter;
import java.io.IOException;
import java.io.Reader;
import java.util.ArrayList;

import org.apache.solr.common.SolrInputDocument;

import atos.knowledgelab.capture.exception.CaptureException;

import com.thoughtworks.xstream.XStream;

public class CollectionSerializer {

	public static void serializeToFile (ArrayList<SolrInputDocument> collection, String fileName) throws CaptureException, IOException {
		FileWriter fw = new FileWriter(fileName);
		XStream xstream = new XStream();
		xstream.toXML(collection, fw);
	   
    }
	
	public static ArrayList<SolrInputDocument> deserializeFromFile (String file) throws CaptureException, FileNotFoundException {
		Reader rd = new FileReader(file);
		XStream xstream = new XStream();
		ArrayList<SolrInputDocument> collection = (ArrayList<SolrInputDocument>)xstream.fromXML(rd);
				
		return collection;

	}
}
